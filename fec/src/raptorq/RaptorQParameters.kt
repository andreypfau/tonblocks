private fun Int.isPrime(): Boolean {
    if (this <= 3) return true
    if (this % 2 == 0 || this % 3 == 0) return false

    var i = 5
    var w = 2
    while (i * i <= this) {
        if (this % i == 0) return false
        i += w
        w = 6 - w
    }
    return true
}

